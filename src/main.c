bool isBreakpointPresent(unsigned char *func,
                const size_t *offsets, size_t offsets_count)
{
        for (size_t idx = 0; idx < offsets_count; ++idx) {
                size_t i = offsets[idx];
                if (*(func + i) == 0xCC) {
                        return true;
                }
        }
        return false;
}

static void decrypt(uint8_t *dst, const uint8_t *src, size_t n)
{
        size_t keylen = sizeof(key);
        for (size_t i = 0; i < n; i++) dst[i] = src[i] ^ key[i % keylen];
}

